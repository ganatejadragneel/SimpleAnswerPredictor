def sumi(a,b): #sum of a and b
    return a+b
def subi(a,b):
    if(a>b):
        return a-b
    else:
        return b-a
def mult(a,b):
    return a*b
def divi(a,b):
    if(a>b):
        return a//b
    else:
        return b//a
def mod(a,b):
    if(a>b):
        return a%b
    else:
        return b%a
def greater(a,b):
    if(a>b):
        return a
    else:
        return b
def least(a,b):
    if(a>b):
        return b
    else:
        return a
